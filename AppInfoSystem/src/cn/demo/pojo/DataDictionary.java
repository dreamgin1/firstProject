package cn.demo.pojo;

public class DataDictionary {
	private int id;//主键id
	private String state;//当前状态
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
}
